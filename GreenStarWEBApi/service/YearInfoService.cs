        public static DataTable GetYearInfos()
        {
            ISession session = null;
            DataTable dtGetYearInfos = null;
            try
            {
                session = SessionFactory.GetSession(Clscommon.connectionString);
                if (SessionFactory.IsValidSession(session))
                {
                    List<SQLParameter> parameters = new List<SQLParameter>();
                                        
                    dtGetYearInfos = DataAccessLayer.ExecuteDataRowToDataTable(ApplicationConstants.SPNames.GetYearInfos, parameters, session, "GetYearInfos");
                    SessionFactory.CloseSession(session);
                }
            }
            catch (Exception ex)
            {
                if (session != null)
                {
                    SessionFactory.CloseSession(session);
                }
                Logger.LogError(ex);
                return null;
            }
            return dtGetYearInfos;
        }

